package whileLoop;

public class WhileLoop {

	public static void main(String[] args) {
		

		int a=0;
		
		while(a<=100) {
			if(a%2==0) {
				System.out.print(" "+a);
			}
			a++;
		
		} 
		a=0;
		System.out.println();
		
		while(a<=100) {
			if(a%2==1) {
				System.out.print(" "+a);
			}
		a++;
	}
	
	
	
}

}
