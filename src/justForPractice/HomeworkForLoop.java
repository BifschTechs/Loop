package justForPractice;

public class HomeworkForLoop {

	public static void main(String[] args) {

		String point="....";
		
		int pointNum=point.length();
		
		int number=1;
		
		
		for(pointNum=4; pointNum>=0 && number<=5; pointNum-=1,number++) {
																																																																																																																																																																		
																																																																																																																																																																		
			System.out.print(point.substring(0, pointNum));
			System.out.println(number);
		}
		
	}

}
